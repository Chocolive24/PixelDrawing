#pragma once

#include "MiniFB.h"

// Variables 
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------

mfb_timer* timer = mfb_timer_create();
double deltaTime = 0.0, totalTime = 0.0;

// ---------------------------------------------------------------------------------------------------------------------------------------------------------------

// Functions 
// ---------------------------------------------------------------------------------------------------------------------------------------------------------------

void TimerTick()
{
    deltaTime = mfb_timer_delta(timer);
    totalTime += deltaTime;
}